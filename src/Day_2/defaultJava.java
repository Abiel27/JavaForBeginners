package Day_2;

public class defaultJava {
    public static void main(String[] args) {


//    The default Statement
//    What is the output of the following code?
        int a = 1;
        int b = 12;
        int c = 40;
        switch (a) {
            case 40:
                System.out.println(b);
                break;
            default:
                System.out.println(c);
        }
    }
}